#include <nitrate-ir/IRGraph.hh>
#include <passes/PassList.hh>

/**
 * @brief Ensure that no nodes are nullptr or have missing module pointers.
 *
 * @timecomplexity O(n)
 * @spacecomplexity O(1)
 */

using namespace nr;

bool nr::pass::ds_nullchk(qmodule_t *mod, IReport *log) {
  bool has_bad_null = false;

  const auto cb = [&](Expr *, Expr **_cur) -> IterOp {
    if (*_cur == nullptr) [[unlikely]] {
      has_bad_null = true;

      log->report(DSNullPtr, IC::FatalError, "");
      return IterOp::Abort;
    }

    return IterOp::Proceed;
  };

  iterate<IterMode::dfs_pre>(mod->getRoot(), cb);

  return !has_bad_null;
}
