void AST_Reader::parse_json() {
  let reader = AST_JsonReader(m_is);

  m_okay = reader.okay();
  m_root = reader.get().value_or(nullptr);
}

void AST_Reader::parse_msgpack() {
  let reader = AST_MsgPackReader(m_is);

  m_okay = reader.okay();
  m_root = reader.get().value_or(nullptr);
}
