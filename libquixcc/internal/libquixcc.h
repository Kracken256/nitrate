#ifndef __QUIXCC_QUIXCCLIB_H__
#define __QUIXCC_QUIXCCLIB_H__

#ifndef __cplusplus
#error "This header requires C++"
#endif

#include <string>
#include <map>
#include <mutex>
#include <llvm/LLVMWrapper.h>
#include <quixcc.h>

typedef struct quixcc_options_t
{
    const char **m_options;
    uint32_t m_count;
} quixcc_options_t;

typedef struct quixcc_uuid_t
{
    uint8_t data[16];
} quixcc_uuid_t;

struct quixcc_job_t
{
    libquixcc::LLVMContext m_inner;
    std::map<std::string, std::string> m_argset;
    std::mutex m_lock;
    std::string m_triple;
    std::string m_cpu;
    quixcc_uuid_t m_id;
    quixcc_options_t m_options;
    quixcc_status_t m_result;
    FILE *m_in;
    FILE *m_out;
    char *m_filename;
    uint8_t m_priority;
    bool m_debug;
    bool m_tainted;
    bool m_running;
};

#endif // __QUIXCC_QUIXCCLIB_H__