#ifndef __QUIXCC_PREP_MACRO_PARSER_H__
#define __QUIXCC_PREP_MACRO_PARSER_H__

#ifndef __cplusplus
#error "This header requires C++"
#endif

#include <string>
#include <vector>
#include <lexer/Token.h>
#include <quixcc.h>
#include <map>

namespace libquixcc
{
    typedef bool (*MacroParserRoutine)(quixcc_job_t *job, const Token &tok, const std::string &directive, const std::string &parameter, std::vector<libquixcc::Token> &exp);

    class MacroParser
    {
    protected:
        std::map<std::string, MacroParserRoutine> m_routines;
        quixcc_job_t *job;

    public:
        MacroParser(quixcc_job_t &job) : job(&job) {}
        void add_routine(const std::string &name, MacroParserRoutine routine) { m_routines[name] = routine; }
        bool parse(const libquixcc::Token &macro, std::vector<libquixcc::Token> &exp) const;
    };
}

#endif // __QUIXCC_PREP_MACRO_PARSER_H__