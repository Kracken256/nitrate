#ifndef __QUIXCC_PARSE_NODES_STRING_H__
#define __QUIXCC_PARSE_NODES_STRING_H__

#ifndef __cplusplus
#error "This header requires C++"
#endif

#include <string>
#include <vector>
#include <memory>

#include <llvm/LLVMWrapper.h>
#include <parse/nodes/BasicNodes.h>

namespace libquixcc
{
    class StringTypeNode : public TypeNode
    {
        StringTypeNode() { ntype = NodeType::StringTypeNode; }

        static StringTypeNode *m_instance;

    public:
        StringTypeNode(StringTypeNode const &) = delete;
        void operator=(StringTypeNode const &) = delete;

        static StringTypeNode *create()
        {
            if (m_instance == nullptr)
                m_instance = new StringTypeNode();

            return m_instance;
        }

        virtual size_t dfs_preorder(ParseNodePreorderVisitor visitor) override { return visitor.visit(this); }
        virtual std::string to_json(ParseNodeJsonSerializerVisitor visitor) const override { return visitor.visit(this); }
        virtual bool is_composite() const override { return false; }
        virtual size_t size(size_t ptr_size) const override { return ptr_size; }
        virtual std::string to_source() const override { return "string"; }
    };
}

#endif // __QUIXCC_PARSE_NODES_STRING_H__