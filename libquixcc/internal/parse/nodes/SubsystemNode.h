#ifndef __QUIXCC_PARSE_NODES_SUBSYSTEM_H__
#define __QUIXCC_PARSE_NODES_SUBSYSTEM_H__

#ifndef __cplusplus
#error "This header requires C++"
#endif

#include <string>
#include <vector>
#include <memory>

#include <llvm/LLVMWrapper.h>
#include <parse/nodes/BasicNodes.h>

namespace libquixcc
{
    class SubsystemNode : public StmtNode
    {
    public:
        SubsystemNode() { ntype = NodeType::SubsystemNode; }
        SubsystemNode(const std::string &name, const std::set<std::string> &deps, const std::shared_ptr<BlockNode> &block) : m_name(name), m_deps(deps), m_block(block) { ntype = NodeType::SubsystemNode; }

        virtual size_t dfs_preorder(ParseNodePreorderVisitor visitor) override { return visitor.visit(this); }
        virtual std::string to_json(ParseNodeJsonSerializerVisitor visitor) const override { return visitor.visit(this); }
        virtual llvm::Value *codegen(const CodegenVisitor &visitor) const override { return visitor.visit(this); }
        virtual std::unique_ptr<StmtNode> reduce() const override;

        std::string m_name;
        std::set<std::string> m_deps;
        std::shared_ptr<libquixcc::BlockNode> m_block;
    };
}

#endif // __QUIXCC_PARSE_NODES_SUBSYSTEM_H__