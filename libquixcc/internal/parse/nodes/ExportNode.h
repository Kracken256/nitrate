#ifndef __QUIXCC_PARSE_NODES_EXPORTED_H__
#define __QUIXCC_PARSE_NODES_EXPORTED_H__

#ifndef __cplusplus
#error "This header requires C++"
#endif

#include <string>
#include <vector>
#include <memory>

#include <llvm/LLVMWrapper.h>
#include <parse/nodes/BasicNodes.h>

namespace libquixcc
{
    class ExportNode : public StmtNode
    {
    public:
        ExportNode() { ntype = NodeType::ExportNode; }
        ExportNode(std::shared_ptr<StmtNode> stmt) : m_stmt(stmt) { ntype = NodeType::ExportNode; }

        virtual size_t dfs_preorder(ParseNodePreorderVisitor visitor) override { return visitor.visit(std::static_pointer_cast<ExportNode>(shared_from_this())); }
        virtual size_t dfs_postorder(ParseNodePostorderVisitor visitor) override { return visitor.visit(std::static_pointer_cast<ExportNode>(shared_from_this())); }
        virtual std::string to_json(ParseNodeJsonSerializerVisitor visitor) const override { return visitor.visit(this); }

        llvm::Value *codegen(LLVMContext &ctx) const override;

        std::shared_ptr<libquixcc::StmtNode> m_stmt;
    };
}

#endif // __QUIXCC_PARSE_NODES_EXPORTED_H__