#include <lexer-test/suite.h>
#include <quixcc/lexer/Lex.h>

bool libquixcc::test::lexer_test_suite() {
  for (const auto vec : libquixcc::test::lexer_test_vectors) {
    std::vector<Token> tokens;
    if (!StringLexer::QuickLex(vec.raw_src.data(), tokens)) {
      return false;
    }

    if (tokens != vec.tokens) {
      return false;
    }
  }
  return true;
}

const std::vector<libquixcc::test::LexerVector>
    libquixcc::test::lexer_test_vectors = {};