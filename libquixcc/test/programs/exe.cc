#include <quixcc.hpp>
#include <vector>

int main(int argc, char **argv)
{
    std::vector<std::string> args(argv, argv + argc);

    if (args.size() < 2)
    {
        std::cerr << "Usage: " << args[0] << " <source file>" << std::endl;
        return 1;
    }

    if (!quixcc_init())
    {
        std::cerr << "Failed to initialize QuixCC." << std::endl;
        return 1;
    }

    quixcc::Compiler &compiler = quixcc::CompilerBuilderFactory::createExecutable()
                                    .in(args[1])
                                    .out("a.out")
                                    .build()
                                    .run()
                                    .puts();

    if (!compiler.ok())
    {
        std::cerr << "Compilation failed." << std::endl;
        return 1;
    }   

    return 0;
}