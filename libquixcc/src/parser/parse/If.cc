#define QUIXCC_INTERNAL

#include <parse/Parser.h>
#include <LibMacro.h>
#include <error/Message.h>

using namespace libquixcc;

bool libquixcc::parse_if(quixcc_job_t &job, std::shared_ptr<libquixcc::Scanner> scanner, std::shared_ptr<libquixcc::StmtNode> &node)
{

    std::shared_ptr<ExprNode> cond;
    if (!parse_expr(job, scanner, Token(TokenType::Punctor, Punctor::OpenBrace), cond))
        return false;

    std::shared_ptr<BlockNode> then_block;
    if (!parse(job, scanner, then_block, true))
        return false;

    Token tok = scanner->peek();
    if (tok.type() == TokenType::Keyword && std::get<Keyword>(tok.val()) == Keyword::Else)
    {
        scanner->next();
        std::shared_ptr<BlockNode> else_block;
        if (!parse(job, scanner, else_block, true))
            return false;

        node = std::make_shared<IfStmtNode>(cond, then_block, else_block);
    }
    else
    {
        node = std::make_shared<IfStmtNode>(cond, then_block, nullptr);
    }

    return true;
}
