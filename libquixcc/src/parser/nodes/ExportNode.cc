#define QUIXCC_INTERNAL

#include <parse/nodes/ExportNode.h>

llvm::Value *libquixcc::ExportNode::codegen(libquixcc::LLVMContext &ctx) const
{
    bool pub = ctx.m_pub;

    ctx.m_pub = true;

    auto stmt = m_stmt->codegen(ctx);

    if (!pub)
        ctx.m_pub = false;

    return stmt;
}