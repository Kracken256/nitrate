#define QUIXCC_INTERNAL

#include <preprocessor/macro/UseMacro.h>
#include <lexer/Lex.h>
#include <core/Logger.h>
#include <regex>

static bool match_format(const std::string &str)
{
    static std::regex re("v[0-9]+\\.[0-9]+");
    return std::regex_match(str, re);
}

bool libquixcc::macro::ParseUse(quixcc_job_t *job, const Token &tok, const std::string &directive, const std::string &parameter, std::vector<libquixcc::Token> &exp)
{
    (void)tok;
    (void)directive;

    StringLexer lexer(parameter);

    Token tok2 = lexer.next();
    if (tok2.type() != TT::String)
    {
        LOG(ERROR) << "Invalid parameter for use directive" << tok << std::endl;
        return false;
    }

    std::string version = std::get<std::string>(tok2.val());

    if (!match_format(version))
    {
        LOG(ERROR) << "Invalid version format for use directive" << tok << std::endl;
        return false;
    }

    uint major = std::stoi(version.substr(1, version.find('.')));
    uint minor = std::stoi(version.substr(version.find('.') + 1));

    if (major != 1 && minor != 0)
    {
        LOG(WARN) << "Unsupported QUIX version" << tok << std::endl;
    }

    return true;
}