#define QUIXCC_INTERNAL

#include <LibMacro.h>
#include <core/Logger.h>
#include <parsetree/Parser.h>

using namespace libquixcc;

bool libquixcc::parse_typedef(quixcc_job_t &job, libquixcc::Scanner *scanner,
                              std::shared_ptr<libquixcc::StmtNode> &node) {
  /*
  type <name> = <type>;
  */

  Token tok = scanner->next();
  if (tok.type() != tName) {
    LOG(ERROR) << core::feedback[TYPEDEF_EXPECTED_IDENTIFIER] << tok
               << std::endl;
    return false;
  }

  std::string name = tok.as<std::string>();

  tok = scanner->next();
  if (!tok.is<Operator>(Operator::Assign)) {
    LOG(ERROR) << core::feedback[TYPEDEF_EXPECTED_ASSIGN] << tok << std::endl;
    return false;
  }

  std::shared_ptr<TypeNode> type;
  if (!parse_type(job, scanner, type)) {
    LOG(ERROR) << core::feedback[TYPEDEF_INVALID_TYPE] << name << tok
               << std::endl;
    return false;
  }

  tok = scanner->next();
  if (!tok.is<Punctor>(Semicolon)) {
    LOG(ERROR) << core::feedback[TYPEDEF_EXPECTED_SEMICOLON] << tok
               << std::endl;
    return false;
  }

  node = std::make_shared<TypedefNode>(type, name);

  return true;
}
