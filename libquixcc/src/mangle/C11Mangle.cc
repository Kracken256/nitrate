    case libquixcc::NodeType::LetDeclNode:
        return static_cast<const libquixcc::LetDeclNode *>(node)->m_name;
    case libquixcc::NodeType::FunctionDeclNode:
        return static_cast<const libquixcc::FunctionDeclNode *>(node)->m_name;
