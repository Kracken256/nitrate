#define QUIXCC_INTERNAL

#include <mutate/Routine.h>
#include <stack>
#include <queue>

using namespace libquixcc;

static void fold_const_string_expr(std::string _namespace, libquixcc::ParseNode *parent, std::shared_ptr<libquixcc::ParseNode> *node)
{
    (void)_namespace;

    switch ((*node)->ntype)
    {
    case NodeType::ConstUnaryExprNode:
    {
        parent->replace_child(*node, std::static_pointer_cast<libquixcc::ConstUnaryExprNode>(*node)->reduce());
        return;
    }
    case NodeType::ConstBinaryExprNode:
        parent->replace_child(*node, std::static_pointer_cast<libquixcc::ConstBinaryExprNode>(*node)->reduce());
        break;
    default:
        return;
    }
}

void libquixcc::mutate::FoldConstExpr(quixcc_job_t *job, std::shared_ptr<libquixcc::AST> ast)
{
    ast->dfs_preorder(ParseNodePreorderVisitor(fold_const_string_expr, job->m_inner.prefix));
}