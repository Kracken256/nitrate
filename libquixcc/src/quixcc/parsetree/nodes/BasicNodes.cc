static void nop(const std::vector<std::string> &_namespace,
                const std::vector<std::string> &_scope, ParseNode *parrent,
                traversal::TraversePtr node) {
  (void)_namespace;
  (void)_scope;
  (void)parrent;
  (void)node;
  return;
}

size_t ParseNode::count() { return dfs_preorder(nop); }
