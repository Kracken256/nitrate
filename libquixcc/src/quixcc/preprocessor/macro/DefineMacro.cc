#define QUIXCC_INTERNAL

#include <quixcc/core/Logger.h>
#include <quixcc/preprocessor/Preprocessor.h>

#include <iostream>

bool libquixcc::PrepEngine::ParseDefine(const Token &tok, const std::string &directive,
                                        const std::string &parameter) {
  (void)job;
  (void)tok;
  (void)directive;

  auto lex = clone();
  lex->set_source(parameter, "<define-macro>");
  std::string dname;

  lex->disable_expansion();
  Token t = lex->next();
  lex->enable_expansion();

  if (t.type() != tName) {
    LOG(ERROR) << "Expected identifier in @define directive" << t << std::endl;
    return false;
  }
  dname = t.as<std::string>();

  t = lex->next();
  if (!t.is<Operator>(OpAssign)) {
    LOG(ERROR) << "Expected '=' in @define directive" << t << std::endl;
    return false;
  }

  lex->disable_expansion();
  t = lex->next();
  lex->enable_expansion();
  std::string dvalue = t.serialize(false);

  set_static(dname, dvalue);

  return true;
}